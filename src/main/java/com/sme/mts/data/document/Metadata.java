package com.sme.mts.data.document;

/**
 * 元数据基类
 */
public class Metadata {

    /**
     * 唯一标识
     */
    protected String id;

    /**
     * 元数据名称
     */
    protected String name;

    /**
     * 元数据值
     */
    protected String value;

    /**
     * 元数据描述
     */
    protected String description;

    /**
     * 元数据分类
     */
    protected String catalog;
}
